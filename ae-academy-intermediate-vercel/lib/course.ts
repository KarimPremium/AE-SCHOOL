export const FLAT_LESSONS = INTERMEDIATE_MODULES.flatMap((module) =>
  module.lessons.map((item) => ({ ...item, moduleId: module.id, moduleTitle: module.title })),
);

export const TOTAL_MINUTES = FLAT_LESSONS.reduce((sum, item) => sum + item.duration, 0);

export function formatDuration(totalMinutes: number) {
  const hours = Math.floor(totalMinutes / 60);
  const minutes = totalMinutes % 60;
  return minutes ? `${hours} h ${minutes.toString().padStart(2, "0")}` : `${hours} h`;
}

export function getResources(ids: string[]) {
  return ids
    .map((id) => RESOURCES.find((resource) => resource.id === id))
    .filter((resource): resource is Resource => Boolean(resource));
}
